// =============================================================
// Utils.h
// 게임 전체에서 공통으로 사용하는 유틸리티 함수들의 선언 파일.
// 실제 구현(내용)은 Utils.cpp에 있고,
// 이 헤더 파일은 "이런 함수가 있다"고 다른 파일에 알려주는 역할만 한다.
//
// 다른 .cpp 파일에서 #include "Utils.h" 를 하면
// 아래 함수들을 사용할 수 있게 된다.
// =============================================================

#pragma once        // 이 헤더 파일이 같은 컴파일 단위에서 두 번 이상 포함되지 않도록 막는다.
                    // 헤더 파일 맨 위에 항상 쓰는 관용구다.
#include <string>   // std::string 타입을 사용하기 위해 포함


// -------------------------------------------------------------
// randInt()
// 0 이상 max 이하의 랜덤 정수를 하나 반환한다.
// 범위 안의 모든 정수가 뽑힐 확률이 동일하다.
//
// 기획서 6.2.1: "모든 확률은 그 범위의 모든 정수가 각각 같은 확률"
//
// 예시: randInt(4) 를 호출하면 0, 1, 2, 3, 4 중 하나가 반환됨 (각 20%)
//
// 매개변수: max - 범위의 최댓값 (포함)
// 반환값:   0 이상 max 이하의 정수
// -------------------------------------------------------------
int randInt(int max);


// -------------------------------------------------------------
// randIntRange()
// min 이상 max 이하의 랜덤 정수를 하나 반환한다.
// randInt()는 항상 0부터 시작하지만, 이 함수는 시작점을 직접 지정할 수 있다.
//
// 매개변수: min - 범위의 최솟값 (포함)
//           max - 범위의 최댓값 (포함)
// 반환값:   min 이상 max 이하의 정수
// -------------------------------------------------------------
int randIntRange(int min, int max);


// -------------------------------------------------------------
// printLine()
// 콘솔에 구분선("===...===")을 출력한다.
// 메뉴, 순위표, 로스터 등 화면 구역을 나눌 때 사용한다.
// -------------------------------------------------------------
void printLine();


// -------------------------------------------------------------
// waitEnter()
// "[계속하려면 Enter를 누르세요...]" 메시지를 출력하고
// 플레이어가 Enter를 누를 때까지 대기한다.
// 경기 결과나 중요 정보를 플레이어가 읽을 시간을 주기 위해 사용한다.
// -------------------------------------------------------------
void waitEnter();
