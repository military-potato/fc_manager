// =============================================================
// Player.h
// 선수 한 명을 표현하는 Player 클래스의 선언 파일.
//
// 기획서 6.1:
//   모든 선수는 공격, 미드, 수비 세 가지 스탯을 가진다.
//   각 스탯은 0~100 사이의 무작위 정수다.
//   선수는 포메이션에 따라 수비수/미드필더/공격수 역할을 부여받는다.
//
// 이 헤더를 include하면 Player 클래스와 관련 함수들을 사용할 수 있다.
// =============================================================

#pragma once        // 중복 include 방지
#include <string>   // std::string (선수 이름 저장에 사용)


// -------------------------------------------------------------
// Role (역할 열거형)
// 선수가 포메이션에서 맡는 역할을 나타낸다.
//
// enum class: 일반 enum보다 안전한 열거형.
//             Role::DEFENDER 처럼 타입 이름을 앞에 붙여서 사용한다.
//             다른 enum의 값과 이름이 겹쳐도 충돌하지 않는다.
// -------------------------------------------------------------
enum class Role {
    DEFENDER,    // 수비수: 수비스탯이 팀 수비 수치에 반영됨
    MIDFIELDER,  // 미드필더: 미드스탯이 공격/수비 양쪽에 20% 반영됨
    FORWARD      // 공격수: 공격스탯이 팀 공격 수치에 반영됨
};


// -------------------------------------------------------------
// roleToString()
// Role 열거형 값을 한글 문자열로 변환한다.
// 로스터 출력 시 역할을 사람이 읽을 수 있는 형태로 보여주기 위해 사용한다.
//
// 예: Role::DEFENDER -> "수비수"
// -------------------------------------------------------------
std::string roleToString(Role r);


// =============================================================
// Player 클래스
// 선수 한 명의 이름, 스탯, 역할을 묶어 관리하는 클래스.
// =============================================================
class Player {
public:
    // 선수 이름 (예: "Marcus Silva")
    std::string name;

    // 공격 스탯: 0~100 사이 정수. 공격수로 배치될 때 팀 공격 수치에 반영된다.
    int attackStat;

    // 미드 스탯: 0~100 사이 정수. 미드필더로 배치될 때 공격/수비 양쪽에 20% 반영된다.
    int midStat;

    // 수비 스탯: 0~100 사이 정수. 수비수로 배치될 때 팀 수비 수치에 반영된다.
    int defStat;

    // 현재 포메이션에서 이 선수에게 부여된 역할
    // applyFormation()이 호출될 때마다 포메이션에 맞게 자동으로 설정된다.
    Role role;

    // ----------------------------------------------------------
    // 생성자: 이름을 받아서 선수를 초기화한다.
    // 스탯은 0~100 사이 무작위 정수로 자동 설정된다.
    // 역할은 일단 MIDFIELDER로 초기화하고,
    // 나중에 Team::applyFormation()에서 실제 역할로 바뀐다.
    //
    // 매개변수: name - 선수 이름 문자열
    // ----------------------------------------------------------
    Player(const std::string& name);

    // ----------------------------------------------------------
    // printInfo()
    // 이 선수의 정보를 콘솔에 한 줄로 출력한다.
    // 로스터 화면과 트레이드 화면에서 사용된다.
    //
    // 출력 형식 예시:
    //   Marcus Silva           | 공격: 72  미드: 45  수비: 88  [수비수]
    //
    // const: 이 함수가 선수 데이터를 변경하지 않음을 보장
    // ----------------------------------------------------------
    void printInfo() const;
};


// -------------------------------------------------------------
// generateRandomName()
// NamePool.h의 FIRST_NAMES와 LAST_NAMES에서 각각 하나씩 랜덤으로 골라
// "이름 성" 형태의 문자열을 반환한다.
//
// 반환값: 예시 "Marcus Silva", "Hiroshi Tanaka" 등
// -------------------------------------------------------------
std::string generateRandomName();


// -------------------------------------------------------------
// generateRandomPlayer()
// 랜덤 이름과 랜덤 스탯을 가진 선수 객체를 생성해서 반환한다.
// Team 생성자와 트레이드 시 새 선수를 만들 때 사용한다.
// -------------------------------------------------------------
Player generateRandomPlayer();
