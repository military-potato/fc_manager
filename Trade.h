// =============================================================
// Trade.h
// 선수 트레이드 관련 함수의 선언 파일.
//
// 기획서 6.4: 선수 트레이드
//   - 경기 결과에 따라 트레이드 횟수가 달라짐 (승/무 = 1회, 패배 = 2회)
//   - 영입 후보 3명 중 1명을 선택해서 영입
//   - 현재 roster에서 1명을 선택해서 방출
//   - 항상 10명 인원 유지 (1대1 교환 구조)
// =============================================================

#pragma once         // 중복 include 방지
#include "Team.h"    // Team 클래스 (선수 명단 수정에 사용)
#include "Match.h"   // MatchResult 열거형 (트레이드 횟수 계산에 사용)


// -------------------------------------------------------------
// calcTradeCount()
// 경기 결과에 따라 가능한 트레이드 횟수를 반환한다.
//
// 기획서 6.4: 승리/무승부 = 1회, 패배 = 2회
// 패배 시 트레이드 횟수가 더 많은 이유:
//   약한 팀을 보강할 기회를 더 주어 다음 경기에서 역전을 기대할 수 있게 함
//
// 매개변수: result - 직전 경기 결과 (WIN/DRAW/LOSS)
// 반환값:   가능한 트레이드 횟수 (1 또는 2)
// -------------------------------------------------------------
int calcTradeCount(MatchResult result);


// -------------------------------------------------------------
// doTrade()
// 트레이드 1회를 진행한다.
//
// 진행 순서:
//   1. 영입 후보 선수 3명 생성 및 출력
//   2. 플레이어가 번호 입력으로 영입할 선수 선택
//   3. 현재 roster 출력
//   4. 플레이어가 번호 입력으로 방출할 선수 선택
//   5. 선택한 선수 교체 후 포메이션 역할 재배정
//
// 매개변수: playerTeam - 플레이어 팀 (참조로 받아서 직접 수정)
// -------------------------------------------------------------
void doTrade(Team& playerTeam);


// -------------------------------------------------------------
// runTrades()
// 트레이드 전체를 관리한다.
// calcTradeCount()로 횟수를 구하고, 각 횟수마다 doTrade()를 호출한다.
// 플레이어가 "n"을 입력하면 해당 횟수의 트레이드를 건너뛸 수 있다.
//
// 매개변수:
//   playerTeam  - 플레이어 팀 (참조로 받아서 직접 수정)
//   lastResult  - 직전 경기 결과 (트레이드 횟수 결정에 사용)
// -------------------------------------------------------------
int runTrades(Team& playerTeam, int tradeCount);
