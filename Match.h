// =============================================================
// Match.h
// 경기 진행과 관련된 구조체, 열거형, 함수의 선언 파일.
//
// 기획서 관련 항목:
//   6.2.1 - 경기결과 결정 메커니즘 (득점 값 결정, 승무패 비교)
//   4.2   - 플레이오프 승부차기
//
// 실제 경기 로직은 Match.cpp에 구현되어 있다.
// =============================================================

#pragma once        // 중복 include 방지
#include "Team.h"   // Team 클래스 (경기에 참여하는 두 팀)
#include <string>   // std::string (팀 이름 출력에 사용)


// =============================================================
// MatchResult 열거형
// 경기 결과를 나타낸다. 항상 홈 팀 기준으로 해석한다.
//
// 예: resultForHome == WIN 이면 홈 팀이 이긴 것.
//     플레이어가 어웨이일 경우 결과를 반전해서 해석해야 한다.
// =============================================================
enum class MatchResult {
    WIN,   // 홈 팀 승리
    DRAW,  // 무승부
    LOSS   // 홈 팀 패배 (어웨이 팀 승리)
};


// =============================================================
// MatchScore 구조체
// 경기 한 번의 결과 데이터를 담는 구조체.
// playMatch() 함수가 이 구조체를 반환한다.
// =============================================================
struct MatchScore {
    int homeGoals;            // 홈 팀이 넣은 골 수
    int awayGoals;            // 어웨이 팀이 넣은 골 수
    MatchResult resultForHome; // 홈 팀 기준의 경기 결과 (WIN/DRAW/LOSS)
};


// -------------------------------------------------------------
// playMatch()
// 홈 팀과 어웨이 팀이 경기를 진행하고 결과를 반환한다.
// 득점 최댓값을 계산하고, 그 범위에서 랜덤 득점을 뽑아 비교한다.
// 경기가 끝나면 두 팀의 승점/승무패/득실 기록을 자동으로 업데이트한다.
//
// 기획서 6.2.1: 득점 값은 [0, 득점 최댓값] 범위의 균등 랜덤 정수
//
// 매개변수:
//   home        - 홈 팀 객체 (참조: 경기 후 성적이 이 객체에 직접 반영됨)
//   away        - 어웨이 팀 객체
//   printDetail - true이면 경기 결과를 콘솔에 출력 (기본값: false)
//                 플레이어 경기는 true, AI끼리 경기는 false로 설정
//
// 반환값: MatchScore 구조체 (골 수와 홈 팀 기준 결과)
// -------------------------------------------------------------
MatchScore playMatch(Team& home, Team& away, bool printDetail = false);


// -------------------------------------------------------------
// playoffTiebreak()
// 플레이오프에서 무승부가 나왔을 때 승부차기를 진행한다.
// 기획서 6.7.1 (4): 두 팀 모두 50% 확률로 승리
//
// 매개변수:
//   teamA - 현재까지 살아남은 팀 이름 (survivor)
//   teamB - 도전하는 팀 이름 (challenger)
//
// 반환값: true이면 teamA 승리, false이면 teamB 승리
// -------------------------------------------------------------
bool playoffTiebreak(const std::string& teamA, const std::string& teamB);
