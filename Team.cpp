// =============================================================
// FORMATIONS 배열 정의
// Team.h에서 extern으로 "이 배열이 어딘가에 있다"고 선언만 해뒀고,
// 실제 데이터는 여기서 딱 한 번 정의한다.
//
// Formation 구조체는 { 수비수 수, 미드필더 수, 공격수 수, 이름 } 순서.
// 기획서 6.3.2: 포메이션 10가지, 숫자 순서는 수비-미드-공격
// =============================================================
const Formation FORMATIONS[10] = {
    {3, 5, 2, "3-5-2"},  // 수비 3명, 미드 5명, 공격 2명
    {3, 4, 3, "3-4-3"},  // 수비 3명, 미드 4명, 공격 3명
    {3, 6, 1, "3-6-1"},  // 수비 3명, 미드 6명, 공격 1명
    {4, 4, 2, "4-4-2"},  // 수비 4명, 미드 4명, 공격 2명 (기본값, formationIndex=3)
    {4, 3, 3, "4-3-3"},  // 수비 4명, 미드 3명, 공격 3명
    {4, 5, 1, "4-5-1"},  // 수비 4명, 미드 5명, 공격 1명
    {4, 2, 4, "4-2-4"},  // 수비 4명, 미드 2명, 공격 4명
    {5, 3, 2, "5-3-2"},  // 수비 5명, 미드 3명, 공격 2명
    {5, 4, 1, "5-4-1"},  // 수비 5명, 미드 4명, 공격 1명
    {5, 2, 3, "5-2-3"}   // 수비 5명, 미드 2명, 공격 3명
};

// 포메이션 종류 개수. FORMATIONS 배열 크기와 항상 일치해야 한다.
// Game.cpp에서 포메이션 목록을 출력할 때 반복 횟수로 사용한다.
const int FORMATION_COUNT = 10;


// =============================================================
// tacticToString()
// Tactic 열거형 값을 한글 문자열로 변환한다.
//
// 매개변수: t - 변환할 전술 (ATTACK / BALANCE / DEFENSE 중 하나)
// 반환값:   해당 전술의 한글 이름 문자열
// =============================================================
std::string tacticToString(Tactic t) {
    switch (t) {                               // t 값에 따라 분기
        case Tactic::ATTACK:  return "공격적 전술";  // 공격 전술
        case Tactic::BALANCE: return "균형적 전술";  // 균형 전술
        case Tactic::DEFENSE: return "수비적 전술";  // 수비 전술
    }
    return "?";  // 정상적으로는 여기까지 오지 않음. 예외 처리용.
}


// =============================================================
// attackBonus() - 전술에 따른 공격 보정치 반환
//
// 기획서 6.2.2:
//   공격적 전술 -> 공격 보정치 70
//   균형적 전술 -> 공격 보정치 60
//   수비적 전술 -> 공격 보정치 50
//
// static: 이 파일 안에서만 사용하는 내부 함수.
//         다른 .cpp 파일에서는 이 함수를 직접 호출할 수 없다.
// =============================================================
static int attackBonus(Tactic t) {
    switch (t) {
        case Tactic::ATTACK:  return 70;  // 공격 전술: 공격 보정 최대
        case Tactic::BALANCE: return 60;  // 균형 전술: 중간
        case Tactic::DEFENSE: return 50;  // 수비 전술: 공격 보정 최소
    }
    return 60;  // 예외 상황 기본값
}


// =============================================================
// defenseBonus() - 전술에 따른 수비 보정치 반환
//
// 기획서 6.2.2:
//   공격적 전술 -> 수비 보정치 0
//   균형적 전술 -> 수비 보정치 10
//   수비적 전술 -> 수비 보정치 20
//
// 공격 보정치와 반대 방향으로 움직인다.
// 공격을 올리면 수비가 내려가고, 수비를 올리면 공격이 내려가는 구조.
// =============================================================
static int defenseBonus(Tactic t) {
    switch (t) {
        case Tactic::ATTACK:  return 0;   // 공격 전술: 수비 보정 없음
        case Tactic::BALANCE: return 10;  // 균형 전술: 중간
        case Tactic::DEFENSE: return 20;  // 수비 전술: 수비 보정 최대
    }
    return 10;  // 예외 상황 기본값
}


// =============================================================
// Team 생성자
// 팀 이름을 받아서 팀 객체를 초기화한다.
// 선수 10명을 자동으로 생성하고 기본 포메이션을 적용한다.
//
// 콜론(:) 뒤의 초기화 목록으로 멤버 변수들을 바로 설정한다.
//   name(n)              - 팀 이름 저장
//   formationIndex(3)    - 기본 포메이션은 인덱스 3 = "4-4-2"
//   tactic(BALANCE)      - 기본 전술은 균형
//   points(0.0)          - 시즌 시작 승점 0
//   wins/draws/losses    - 승/무/패 모두 0
//   goalsFor/Against     - 득점/실점 모두 0
// =============================================================
Team::Team(const std::string& n)
    : name(n), formationIndex(3), tactic(Tactic::BALANCE),
      points(0.0), wins(0), draws(0), losses(0), goalsFor(0), goalsAgainst(0)
{
    // 기획서 6.1: 플레이어 팀에는 선수 10명
    for (int i = 0; i < 10; ++i)
        roster.push_back(generateRandomPlayer());  // 랜덤 이름/스탯의 선수를 roster에 추가

    // 생성된 10명에게 포메이션에 맞는 역할(수비/미드/공격)을 부여
    applyFormation();
}


// =============================================================
// applyFormation()
// 현재 formationIndex에 해당하는 포메이션을 roster에 적용한다.
// roster의 선수 순서대로 수비수 -> 미드필더 -> 공격수 역할을 부여한다.
//
// 예: 4-4-2 포메이션(formationIndex=3)이면
//   roster[0]~[3]  -> 수비수 4명
//   roster[4]~[7]  -> 미드필더 4명
//   roster[8]~[9]  -> 공격수 2명
// =============================================================
void Team::applyFormation() {
    // 현재 선택된 포메이션 데이터를 참조(reference)로 가져온다.
    // const&: 복사 없이 원본을 읽기 전용으로 참조한다. 성능상 유리하다.
    const Formation& f = FORMATIONS[formationIndex];

    int idx = 0;  // roster에서 현재 역할을 부여할 선수의 위치 (0부터 시작)

    // 수비수 역할 부여: f.def 명만큼 반복 (예: 4-4-2면 f.def=4)
    for (int i = 0; i < f.def; ++i) roster[idx++].role = Role::DEFENDER;
    // idx++: roster[idx]에 역할을 부여하고, idx를 1 증가시킨다.

    // 미드필더 역할 부여: 수비수가 끝난 위치부터 이어서
    for (int i = 0; i < f.mid; ++i) roster[idx++].role = Role::MIDFIELDER;

    // 공격수 역할 부여: 미드필더가 끝난 위치부터 이어서
    for (int i = 0; i < f.fwd; ++i) roster[idx++].role = Role::FORWARD;
}


// =============================================================
// changeFormation()
// 플레이어가 새 포메이션을 선택했을 때 호출된다.
// formationIndex를 바꾸고 applyFormation()으로 역할을 재배정한다.
//
// 매개변수: idx - FORMATIONS 배열의 인덱스 (0~9)
// =============================================================
void Team::changeFormation(int idx) {
    formationIndex = idx;  // 새로 선택한 포메이션 번호로 업데이트
    applyFormation();      // 바뀐 포메이션 기준으로 선수 역할 재배정
}


// =============================================================
// getAttackValue()
// 이 팀의 공격 수치를 계산해서 반환한다.
//
// 기획서 6.2.3 (1) 공격 수치 계산식:
//   공격 수치 = 공격수 공격스탯 평균 + 미드필더 미드스탯 평균 * 20% + 공격 보정치
//
// const: 이 함수가 팀 데이터를 읽기만 하고 변경하지 않는다는 보장
// =============================================================
double Team::getAttackValue() const {
    double fwdAvg = 0;  // 공격수들의 공격스탯 합계 (나중에 나눠서 평균을 구함)
    double midAvg = 0;  // 미드필더들의 미드스탯 합계
    int fwdCnt = 0;     // 공격수 인원 수 (0으로 나누기 방지용)
    int midCnt = 0;     // 미드필더 인원 수

    // roster의 모든 선수를 순회하면서 역할에 따라 스탯을 누적
    for (int i = 0; i < (int)roster.size(); ++i) {
        if (roster[i].role == Role::FORWARD) {      // 이 선수가 공격수이면
            fwdAvg += roster[i].attackStat;         // 공격스탯을 합계에 더함
            fwdCnt++;                               // 공격수 수 1 증가
        }
        if (roster[i].role == Role::MIDFIELDER) {   // 이 선수가 미드필더이면
            midAvg += roster[i].midStat;            // 미드스탯을 합계에 더함
            midCnt++;                               // 미드필더 수 1 증가
        }
    }

    // 합계를 인원 수로 나눠서 평균값으로 변환
    // 인원이 0명이면 나누지 않는다. (0으로 나누면 프로그램이 오류 발생)
    if (fwdCnt > 0) fwdAvg /= fwdCnt;  // 공격수 공격스탯 평균
    if (midCnt > 0) midAvg /= midCnt;  // 미드필더 미드스탯 평균

    // 기획서 6.2.3 계산식:
    //   공격수 공격스탯 평균
    //   + 미드필더 미드스탯 평균 * 0.2  (기획서의 20%)
    //   + 공격 보정치 (전술에 따라 50/60/70)
    return fwdAvg + midAvg * 0.2 + attackBonus(tactic);
}


// =============================================================
// getDefenseValue()
// 이 팀의 수비 수치를 계산해서 반환한다.
//
// 기획서 6.2.3 (2) 수비 수치 계산식:
//   수비 수치 = 수비수 수비스탯 평균 + 미드필더 미드스탯 평균 * 20% + 수비 보정치
//
// getAttackValue()와 구조가 거의 같고, 참조하는 스탯과 보정치만 다르다.
// =============================================================
double Team::getDefenseValue() const {
    double defAvg = 0;  // 수비수들의 수비스탯 합계
    double midAvg = 0;  // 미드필더들의 미드스탯 합계
    int defCnt = 0;     // 수비수 인원 수
    int midCnt = 0;     // 미드필더 인원 수

    for (int i = 0; i < (int)roster.size(); ++i) {
        if (roster[i].role == Role::DEFENDER) {     // 수비수이면
            defAvg += roster[i].defStat;            // 수비스탯 누적
            defCnt++;
        }
        if (roster[i].role == Role::MIDFIELDER) {   // 미드필더이면
            midAvg += roster[i].midStat;            // 미드스탯 누적
            midCnt++;
        }
    }

    if (defCnt > 0) defAvg /= defCnt;  // 수비수 수비스탯 평균
    if (midCnt > 0) midAvg /= midCnt;  // 미드필더 미드스탯 평균

    // 기획서 6.2.3 계산식:
    //   수비수 수비스탯 평균
    //   + 미드필더 미드스탯 평균 * 0.2
    //   + 수비 보정치 (전술에 따라 0/10/20)
    return defAvg + midAvg * 0.2 + defenseBonus(tactic);
}


// =============================================================
// calcMaxGoals()
// 이 팀이 상대 팀과 경기할 때의 득점 최댓값을 계산한다.
// 이 값이 클수록 높은 점수를 뽑을 확률이 높아진다.
//
// 기획서 6.2.3 (3) 득점 최댓값 계산식:
//   (내 공격 수치 - 상대 수비 수치) / 20  ->  반올림
//
// 기획서 6.2.3 (4) 최소 보정:
//   결과가 0 이하이면 1로 고정 (약팀도 최소한의 득점 가능성 보장)
//
// 매개변수: opponent - 상대 팀 객체 (상대의 수비 수치를 가져오기 위해 필요)
// 반환값:   득점 최댓값 (최소 1)
// =============================================================
int Team::calcMaxGoals(const Team& opponent) const {
    // 내 공격 수치에서 상대 수비 수치를 뺀 뒤 20으로 나눈다
    double raw = (getAttackValue() - opponent.getDefenseValue()) / 20.0;

    // 반올림: (int)(raw + 0.5) 는 양수를 반올림하는 관용적인 표현
    // 예: raw=2.3이면 2.8 -> (int)2.8 = 2, raw=2.6이면 3.1 -> (int)3.1 = 3
    int val = (int)(raw + 0.5);

    // 최소 보정: 계산 결과가 0 이하면 1로 고정
    // 아무리 약한 팀이라도 0~1 범위에서 득점 시도 가능
    if (val < 1) val = 1;

    return val;
}


