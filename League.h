// =============================================================
// League.h
// 리그 전체를 관리하는 League 클래스의 선언 파일.
//
// 기획서 관련 항목:
//   4.1  - 리그전: 20팀, 2라운드, 팀당 38경기
//   6.5  - 시즌과 순위표 (승점제: 승리 3점, 무승부 1점, 패배 0점)
//   6.7.2 - 승수 계산 시스템 (승리 시 3.001점)
//
// League 클래스가 하는 일:
//   - 20개 팀 관리 (teams[0] = 플레이어 팀)
//   - 38경기 * 2 = 760개 경기 일정(fixtures) 관리
//   - 플레이어 경기 진행 및 AI 경기 자동 시뮬레이션
//   - 순위표 출력
// =============================================================

#pragma once         // 중복 include 방지
#include "Team.h"    // Team 클래스
#include "Match.h"   // MatchResult 열거형
#include <vector>    // std::vector (팀 목록, 경기 일정 저장)
#include <utility>   // std::move (Team 객체 이동에 사용)


// =============================================================
// Fixture 구조체
// 경기 한 판의 일정 데이터를 담는 구조체.
// homeIdx와 awayIdx는 teams 배열의 인덱스 번호다.
// =============================================================
struct Fixture {
    int homeIdx;  // 홈 팀의 teams 배열 인덱스 (0 = 플레이어 팀)
    int awayIdx;  // 어웨이 팀의 teams 배열 인덱스
    bool played;  // 이 경기가 이미 진행되었는지 여부 (true = 완료)
};


// =============================================================
// League 클래스
// 리그 전체의 상태(팀, 일정, 진행 상황)를 관리한다.
// =============================================================
class League {
public:
    // 리그에 참가하는 20개 팀
    // teams[0] = 플레이어 팀, teams[1]~teams[19] = AI 팀 19개
    std::vector<Team> teams;

    // 리그 전체 경기 일정 목록
    // 1라운드(190경기) + 2라운드(190경기) = 총 380경기
    // (20팀 중 2팀을 고르는 조합: 20 * 19 / 2 = 190개, 2라운드면 380개)
    std::vector<Fixture> fixtures;

    // 다음에 처리할 경기의 fixtures 배열 인덱스
    // 경기가 진행될 때마다 증가한다.
    int currentFixtureIdx;

    // 리그의 모든 플레이어 경기(38경기)가 끝났는지 여부
    // true가 되면 플레이오프로 넘어간다.
    bool leagueFinished;


    // ----------------------------------------------------------
    // 기본 생성자
    // currentFixtureIdx와 leagueFinished를 초기값으로 설정한다.
    // 실제 팀/일정 생성은 init()에서 한다.
    // ----------------------------------------------------------
    League();


    // ----------------------------------------------------------
    // init()
    // 리그를 초기화한다. 새 시즌 시작 시에도 이 함수를 호출한다.
    //   - 기존 팀/일정 데이터 초기화
    //   - 플레이어 팀 1개 + AI 팀 19개 생성
    //   - 2라운드 리그 일정 생성 (모든 팀이 서로 2번씩 경기)
    //
    // 매개변수: playerTeamName - 플레이어 팀의 이름
    // ----------------------------------------------------------
    void init(const std::string& playerTeamName);


    // ----------------------------------------------------------
    // nextPlayerFixtureIdx()
    // 아직 진행하지 않은 플레이어 경기 중 가장 가까운 경기의
    // fixtures 배열 인덱스를 반환한다.
    // 플레이어 경기가 더 없으면 -1을 반환한다.
    //
    // 반환값: fixtures 배열 인덱스, 없으면 -1
    // ----------------------------------------------------------
    int nextPlayerFixtureIdx() const;


    // ----------------------------------------------------------
    // nextOpponentName()
    // 다음 경기의 상대 팀 이름을 반환한다.
    // 메인 메뉴에 "다음 상대: ___" 를 표시할 때 사용한다.
    //
    // 반환값: 상대 팀 이름 문자열, 없으면 빈 문자열("")
    // ----------------------------------------------------------
    std::string nextOpponentName() const;


    // ----------------------------------------------------------
    // simulateAIMatches()
    // AI 팀끼리의 경기를 자동으로 진행한다.
    // 플레이어 경기 직전까지의 AI 경기를 처리할 때 사용한다.
    //
    // 매개변수: upToFixtureIdx - 이 인덱스 직전까지의 경기만 처리
    // ----------------------------------------------------------
    void simulateAIMatches(int upToFixtureIdx);


    // ----------------------------------------------------------
    // playPlayerMatch()
    // 플레이어의 다음 경기를 진행한다.
    // 경기 전에 그 직전까지의 AI 경기를 자동으로 먼저 처리한다.
    //
    // 매개변수: tactic - 플레이어가 선택한 전술
    // 반환값:   경기 결과 (플레이어 팀 기준의 WIN/DRAW/LOSS)
    // ----------------------------------------------------------
    MatchResult playPlayerMatch(Tactic tactic);


    // ----------------------------------------------------------
    // printStandings()
    // 현재 리그 순위표를 콘솔에 출력한다.
    // 플레이어 팀 앞에는 ">" 마커를 붙여서 강조한다.
    // ----------------------------------------------------------
    void printStandings() const;


    // ----------------------------------------------------------
    // getSortedTeamIndices()
    // 승점 내림차순으로 정렬된 팀의 인덱스 배열을 반환한다.
    // 인덱스[0]이 1위 팀, 인덱스[19]가 20위 팀이다.
    // 플레이오프에서 순위 순서대로 팀을 처리할 때도 사용한다.
    //
    // 반환값: teams 배열 인덱스가 승점 순으로 정렬된 vector
    // ----------------------------------------------------------
    std::vector<int> getSortedTeamIndices() const;


    // ----------------------------------------------------------
    // isFinished()
    // 리그가 완전히 끝났는지(플레이어 38경기 모두 완료) 반환한다.
    // ----------------------------------------------------------
    bool isFinished() const;


    // ----------------------------------------------------------
    // playedMatches()
    // 플레이어가 지금까지 진행한 경기 수를 반환한다.
    // 메인 메뉴의 "리그 X/38경기" 표시에 사용한다.
    // ----------------------------------------------------------
    int playedMatches() const;


    // ----------------------------------------------------------
    // totalMatches()
    // 리그에서 플레이어가 치러야 할 총 경기 수를 반환한다.
    // 기획서 4.1: 38경기 (19팀 * 2라운드)
    // ----------------------------------------------------------
    int totalMatches() const;


    // ----------------------------------------------------------
    // playerRank()
    // 현재 순위표에서 플레이어 팀의 순위를 반환한다.
    // 메인 메뉴의 "순위: X위" 표시에 사용한다.
    // ----------------------------------------------------------
    int playerRank() const;
};
