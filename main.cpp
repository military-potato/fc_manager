// =============================================================
// main.cpp
// 프로그램의 진입점(entry point) 파일.
//
// C++ 프로그램은 항상 main() 함수에서 시작된다.
// 이 파일에서는 Game 객체를 하나 만들고 run()을 호출하는 것이 전부다.
// 실제 게임 로직은 모두 Game 클래스와 다른 클래스들에 구현되어 있다.
// =============================================================

#include "Game.h"  // Game 클래스 선언 (start, run 등 게임 전체 흐름 관리)


// =============================================================
// main()
// 프로그램이 실행되면 가장 먼저 호출되는 함수.
// 반환값 0은 "프로그램이 정상 종료되었다"는 의미다.
// =============================================================
int main() {
    Game game;  // Game 객체 생성 (생성자에서 초기 상태 설정)
    game.run(); // 게임 시작 -> 메인 루프 실행 -> 종료까지 여기서 진행
    return 0;   // 운영체제에 정상 종료 신호 반환
}
