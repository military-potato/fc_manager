#pragma once          // 중복 include 방지
#include "League.h"   // League 클래스 (팀 정보와 순위 데이터를 가져오기 위해)
#include <vector>     // std::vector
#include <string>     // std::string (우승 팀 이름 반환에 사용)


// -------------------------------------------------------------
// runPlayoff()
// 리그가 끝난 후 플레이오프를 진행하고 최종 우승 팀 이름을 반환한다.
//
// 진행 방식:
//   1. 리그 순위를 내림차순으로 가져옴 (1위 ~ 20위)
//   2. 20위 팀을 첫 survivor로 설정
//   3. step 18부터 0까지 (19위부터 1위 순서로):
//      - survivor vs challenger(해당 순위 팀) 경기 진행
//      - 이긴 팀이 새 survivor가 됨
//      - 무승부면 승부차기
//   4. 모든 경기가 끝나면 남은 survivor가 우승
//
// 매개변수: league - 리그 전체 데이터 (팀 목록과 순위 정보 포함)
// 반환값:   최종 우승 팀의 이름 문자열
// -------------------------------------------------------------
std::string runPlayoff(League& league);
