//   - 20위 vs 19위, 승자 vs 18위, 승자 vs 17위, ... 승자 vs 1위
//   - 마지막까지 살아남은 팀이 시즌 최종 우승팀
//   - 무승부면 승부차기(50% 확률)로 결정
