#include "Utils.h"   // 이 파일에서 구현할 함수들의 선언
#include <iostream>  // std::cout (콘솔 출력)
#include <random>    // std::mt19937, std::uniform_int_distribution (랜덤 관련)
#include <limits>    // std::numeric_limits (현재는 직접 사용하지 않음)


// -------------------------------------------------------------
// getRNG()
// 랜덤 숫자 생성기(RNG) 객체를 반환하는 내부 함수.
//
// std::mt19937: 메르센 트위스터 알고리즘을 사용하는 고품질 랜덤 생성기.
//              rand()보다 훨씬 균일하고 예측하기 어려운 숫자를 만든다.
//
// static 지역 변수: 이 함수가 처음 호출될 때 딱 한 번 생성되고,
//                   이후 호출에서는 같은 객체를 재사용한다.
//                   매번 새로 만들면 같은 숫자가 반복될 수 있어서 이렇게 한다.
//
// std::random_device{}(): 운영체제가 제공하는 하드웨어 기반 시드 값을 받아온다.
//                         게임을 실행할 때마다 다른 시드로 시작해서
//                         매번 다른 랜덤 결과가 나온다.
//
// static: 이 파일 안에서만 사용하는 내부 함수
// -------------------------------------------------------------
static std::mt19937& getRNG() {
    static std::mt19937 rng(std::random_device{}());  // 최초 호출 시 한 번만 초기화
    return rng;                                        // 이후에는 같은 rng 객체를 반환
}


// -------------------------------------------------------------
// randInt()
// 0 이상 max 이하의 랜덤 정수를 반환한다.
// 기획서 6.2.1: 범위 안의 모든 정수가 동일한 확률로 나와야 한다.
//
// std::uniform_int_distribution: 지정한 범위에서 완전히 균등한
//                                확률로 정수를 뽑아주는 분포 객체.
// -------------------------------------------------------------
int randInt(int max) {
    if (max <= 0) return 0;  // max가 0 이하이면 뽑을 범위가 없으므로 0 반환

    // 0부터 max까지 균등 분포 설정
    std::uniform_int_distribution<int> dist(0, max);
    return dist(getRNG());  // 설정한 분포에서 랜덤 정수 하나를 뽑아 반환
}


// -------------------------------------------------------------
// randIntRange()
// min 이상 max 이하의 랜덤 정수를 반환한다.
// randInt()와 동일한 방식이지만 시작점을 0이 아닌 값으로 지정할 수 있다.
// -------------------------------------------------------------
int randIntRange(int min, int max) {
    if (min >= max) return min;  // min과 max가 같거나 역전되면 min 그대로 반환

    std::uniform_int_distribution<int> dist(min, max);  // min~max 균등 분포
    return dist(getRNG());
}


// -------------------------------------------------------------
// printLine()
// 콘솔에 구분선을 출력한다.
// 화면을 구역별로 나눠서 읽기 편하게 만든다.
// -------------------------------------------------------------
void printLine() {
    std::cout << "================================================\n";  // 48개의 '=' 문자
}


// -------------------------------------------------------------
// waitEnter()
// 안내 메시지를 출력하고 플레이어가 Enter를 누를 때까지 기다린다.
//
// std::getline(std::cin, dummy):
//   키보드 입력 한 줄을 읽어서 dummy 변수에 저장한다.
//   Enter를 누르면 빈 문자열이 저장되고 함수가 계속 진행된다.
//   dummy 변수는 사용하지 않고 버린다. 그냥 Enter 입력을 소비하기 위한 것.
//
// if (!std::getline(...)) return:
//   파이프나 파일로 입력을 줄 때 입력이 끝나면(EOF) getline이 false를 반환한다.
//   이 경우 무한 대기에 빠지지 않도록 바로 함수를 종료한다.
// -------------------------------------------------------------
void waitEnter() {
    std::cout << "\n[계속하려면 Enter를 누르세요...]\n";
    std::string dummy;                              // Enter 입력을 받아 버릴 임시 변수
    if (!std::getline(std::cin, dummy)) return;    // EOF면 그냥 반환
}
