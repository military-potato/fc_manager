// =============================================================
// Game.h
// 게임 전체 흐름을 관리하는 Game 클래스의 선언 파일.
//
// Game 클래스가 하는 일:
//   - 게임 시작 (팀 이름 입력, 리그 초기화)
//   - 메인 루프 실행 (명령어 입력 -> 해당 기능 실행 반복)
//   - 각 메뉴 기능 호출 (일정, 로스터, 트레이드, 순위, 종료)
//   - 리그 종료 시 플레이오프 진행 및 새 시즌 여부 결정
//
// public/private 구분:
//   public  - 외부에서 호출 가능 (main.cpp에서 game.start(), game.run() 호출)
//   private - 클래스 내부에서만 사용 (메뉴 처리 함수들)
// =============================================================

#pragma once          // 중복 include 방지
#include "League.h"   // League 클래스 (게임의 핵심 데이터)
#include <string>     // std::string (팀 이름, 명령어 입력에 사용)


// =============================================================
// Game 클래스
// 게임의 최상위 클래스. 모든 시스템을 여기서 조율한다.
// main.cpp에서 Game 객체를 하나 만들고 run()을 호출하면 게임이 시작된다.
// =============================================================
class Game {
public:
    // 리그 전체 데이터 (팀 20개, 경기 일정, 순위 등)
    League league;

    // 플레이어 팀의 이름 (게임 시작 시 입력받음)
    std::string playerTeamName;

    // 직전 경기 결과 (트레이드 횟수 결정에 사용)
    // 초기값: DRAW (게임 시작 전이므로 임의로 DRAW로 설정)
    MatchResult lastMatchResult;

    // 첫 경기를 진행했는지 여부
    // false이면 트레이드 메뉴 진입 시 "첫 경기 후에만 가능" 안내를 출력
    bool hasPlayedMatch;


    // ----------------------------------------------------------
    // 기본 생성자
    // lastMatchResult를 DRAW로, hasPlayedMatch를 false로 초기화
    // ----------------------------------------------------------
    Game();


    // ----------------------------------------------------------
    // start()
    // 게임 시작 처리를 한다.
    //   - 게임 타이틀과 스토리 출력
    //   - 플레이어에게 팀 이름 입력받기
    //   - league.init()으로 리그(팀 20개, 경기 일정) 초기화
    // ----------------------------------------------------------
    void start();


    // ----------------------------------------------------------
    // run()
    // 게임의 메인 루프를 실행한다.
    //   1. start() 호출로 초기화
    //   2. 메인 메뉴 출력
    //   3. 명령어 입력받기
    //   4. 해당 기능 실행
    //   5. 리그 종료 여부 확인 -> 플레이오프 진행
    //   6. "종료" 입력 전까지 2~5 반복
    // ----------------------------------------------------------
    void run();


private:
    // ----------------------------------------------------------
    // showMainMenu()
    // 메인 메뉴를 콘솔에 출력한다.
    // 현재 팀 이름, 진행 경기 수, 순위, 승점, 다음 상대를 함께 표시한다.
    // ----------------------------------------------------------
    void showMainMenu();


    // ----------------------------------------------------------
    // doSchedule()
    // "일정" 명령어 처리 함수.
    //   1. 다음 상대 팀 이름 출력
    //   2. 전술 선택 (chooseTactic)
    //   3. 경기 진행 (league.playPlayerMatch)
    //   4. 경기 결과 출력
    //   5. 순위표 출력
    // ----------------------------------------------------------
    void doSchedule();


    // ----------------------------------------------------------
    // doRoster()
    // "로스터" 명령어 처리 함수.
    //   - 현재 선수 명단과 포메이션 출력
    //   - 포메이션 변경 여부 확인 후 변경 메뉴 진입
    // ----------------------------------------------------------
    void doRoster();


    // ----------------------------------------------------------
    // doTrade()
    // "트레이드" 명령어 처리 함수.
    //   - 첫 경기 전이면 "첫 경기 후에만 가능" 안내
    //   - 첫 경기 후이면 runTrades() 호출
    // ----------------------------------------------------------
    void doTrade();
    


    // ----------------------------------------------------------
    // changeFormationMenu()
    // 포메이션 변경 서브메뉴.
    // 10가지 포메이션 목록을 보여주고 플레이어가 번호를 입력해 선택한다.
    // ----------------------------------------------------------
    void changeFormationMenu();


    // ----------------------------------------------------------
    // chooseTactic()
    // 경기 전 전술 선택 함수.
    // 3가지 전술(공격/균형/수비)을 보여주고 선택받아 Tactic 값을 반환한다.
    //
    // 반환값: 선택된 전술 (Tactic::ATTACK / BALANCE / DEFENSE)
    // ----------------------------------------------------------
    Tactic chooseTactic();
};
