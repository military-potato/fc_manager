// =============================================================
// Team.h
// 팀 한 개를 표현하는 Team 클래스와 관련 구조체/열거형의 선언 파일.
//
// 기획서 관련 항목:
//   6.1  - 선수 10명, 각 선수는 역할군 보유
//   6.2.2 - 전술 보정치 (Tactic 열거형)
//   6.2.3 - 득점 계산식 (getAttackValue, getDefenseValue, calcMaxGoals)
//   6.3.2 - 포메이션 10가지 (Formation 구조체, FORMATIONS 배열)
//   6.5  - 승점 관리 (points, wins, draws, losses)
// =============================================================

#pragma once          // 중복 include 방지
#include "Player.h"   // Player 클래스, Role 열거형
#include <vector>     // std::vector (선수 목록 roster에 사용)
#include <string>     // std::string (팀 이름에 사용)


// =============================================================
// Formation 구조체
// 포메이션 한 가지의 데이터를 담는 구조체.
// 수비수/미드필더/공격수의 인원 수와 이름표(예: "4-4-2")를 가진다.
//
// 구조체(struct): 관련 데이터를 묶어두는 틀.
//                 클래스와 거의 같지만, 기본적으로 멤버가 public이다.
// =============================================================
struct Formation {
    int def;           // 수비수 인원 수
    int mid;           // 미드필더 인원 수
    int fwd;           // 공격수 인원 수
    const char* label; // 포메이션 이름 문자열 (예: "4-4-2")
                       // const char*: C 스타일 문자열 리터럴을 가리키는 포인터
};


// -------------------------------------------------------------
// FORMATIONS 배열 (extern 선언)
// 기획서 6.3.2의 포메이션 10가지를 담은 배열.
// 실제 데이터는 Team.cpp에서 딱 한 번 정의한다.
//
// extern: "이 변수는 다른 .cpp 파일에 실제로 존재한다"고 컴파일러에 알려주는 키워드.
//          여러 파일에서 include해도 데이터가 중복 생성되지 않는다.
// -------------------------------------------------------------
extern const Formation FORMATIONS[10];  // Team.cpp에 실제 데이터 존재
extern const int FORMATION_COUNT;       // 포메이션 종류 수 (= 10)


// =============================================================
// Tactic 열거형
// 플레이어가 경기 전에 선택하는 전술.
// 전술에 따라 공격/수비 보정치가 달라진다. (기획서 6.2.2)
// =============================================================
enum class Tactic {
    ATTACK,   // 공격적 전술: 공격 보정 +70, 수비 보정 +0
    BALANCE,  // 균형적 전술: 공격 보정 +60, 수비 보정 +10
    DEFENSE   // 수비적 전술: 공격 보정 +50, 수비 보정 +20
};


// -------------------------------------------------------------
// tacticToString()
// Tactic 열거형 값을 한글 문자열로 변환한다.
// 콘솔에 전술 이름을 출력할 때 사용한다.
// -------------------------------------------------------------
std::string tacticToString(Tactic t);


// =============================================================
// Team 클래스
// 팀 한 개의 선수단, 포메이션, 전술, 성적을 관리하는 클래스.
// teams[0]이 플레이어 팀, teams[1]~teams[19]가 AI 팀이다.
// =============================================================
class Team {
public:
    // 팀 이름 (예: "내팀", "Red Dragons FC")
    std::string name;

    // 선수 명단: 항상 10명을 유지한다. (기획서 6.1, 6.3)
    // vector: 크기가 변할 수 있는 동적 배열
    std::vector<Player> roster;

    // 현재 적용된 포메이션의 FORMATIONS 배열 인덱스 (0~9)
    // 기본값은 3 (= "4-4-2")
    int formationIndex;

    // 현재 선택된 전술 (ATTACK / BALANCE / DEFENSE)
    // 기본값은 BALANCE
    Tactic tactic;

    // 리그 누적 승점
    // 기획서 6.7.2: 승리 시 3.001을 더해서 같은 승점일 때 승수 많은 팀이 앞에 오도록 함
    // 순위표에는 (int) 변환으로 소수점을 버리고 정수로 표시한다.
    double points;

    int wins;         // 누적 승리 수
    int draws;        // 누적 무승부 수
    int losses;       // 누적 패배 수
    int goalsFor;     // 누적 득점 (내가 넣은 골)
    int goalsAgainst; // 누적 실점 (상대가 넣은 골)


    // ----------------------------------------------------------
    // 생성자
    // 팀 이름을 받아서 팀을 초기화한다.
    // 선수 10명을 자동 생성하고 기본 포메이션(4-4-2)을 적용한다.
    //
    // 매개변수: name - 팀 이름 문자열
    // ----------------------------------------------------------
    Team(const std::string& name);


    // ----------------------------------------------------------
    // applyFormation()
    // 현재 formationIndex에 해당하는 포메이션을 roster에 적용한다.
    // roster의 선수들에게 수비수/미드필더/공격수 역할을 순서대로 부여한다.
    // 포메이션이 바뀌거나 선수가 교체될 때마다 호출된다.
    // ----------------------------------------------------------
    void applyFormation();


    // ----------------------------------------------------------
    // changeFormation()
    // 포메이션을 변경하고 applyFormation()을 호출해서 역할을 재배정한다.
    //
    // 매개변수: idx - FORMATIONS 배열의 인덱스 (0~9)
    // ----------------------------------------------------------
    void changeFormation(int idx);


    // ----------------------------------------------------------
    // getAttackValue()
    // 기획서 6.2.3 (1): 이 팀의 공격 수치를 계산해서 반환한다.
    //   공격 수치 = 공격수 공격스탯 평균 + 미드필더 미드스탯 평균 * 20% + 공격 보정치
    //
    // const: 이 함수가 팀 데이터를 변경하지 않음을 보장
    // ----------------------------------------------------------
    double getAttackValue() const;


    // ----------------------------------------------------------
    // getDefenseValue()
    // 기획서 6.2.3 (2): 이 팀의 수비 수치를 계산해서 반환한다.
    //   수비 수치 = 수비수 수비스탯 평균 + 미드필더 미드스탯 평균 * 20% + 수비 보정치
    // ----------------------------------------------------------
    double getDefenseValue() const;


    // ----------------------------------------------------------
    // calcMaxGoals()
    // 기획서 6.2.3 (3)(4): 이 팀이 상대 팀과 경기 시 득점 최댓값을 계산한다.
    //   (내 공격 수치 - 상대 수비 수치) / 20, 반올림, 최소 1
    //
    // 매개변수: opponent - 상대 팀 객체
    // 반환값:   득점 최댓값 (최소 1)
    // ----------------------------------------------------------
    int calcMaxGoals(const Team& opponent) const;


    // ----------------------------------------------------------
    // printRoster()
    // 선수 명단, 포메이션, 전술을 콘솔에 출력한다.
    // 로스터 메뉴에서 사용된다.
    // ----------------------------------------------------------
    void printRoster() const;


    // ----------------------------------------------------------
    // displayPoints()
    // 승점을 순위표 표시용 정수로 변환해서 반환한다.
    // 내부 points는 소수점을 포함하지만, 표시는 정수로만 한다.
    // (기획서 6.7.2 참고)
    // ----------------------------------------------------------
    int displayPoints() const;


    // ----------------------------------------------------------
    // printStandingRow()
    // 순위표에서 이 팀의 한 줄(순위, 이름, 승/무/패, 득실차, 승점)을 출력한다.
    // League::printStandings()에서 각 팀마다 호출한다.
    //
    // 매개변수: rank - 현재 이 팀의 순위 번호 (1위, 2위, ...)
    // ----------------------------------------------------------
    void printStandingRow(int rank) const;
};
